#funciones con parametros
def raiz(value):#se define la funcion llamada 'raiz'La función toma un solo argumento llamado value, que es el numero del cual se desea calcular la raiz cuadrada
    return value ** (1/2)#a funcion calcula y devuelve la raiz cuadrada del valor "value" utilizando la operacion value ** (1/2) y luego devuelve el resultado

print(f'La raiz cuadrada es: {raiz(4)}')#se imprime un mensaje que incluye el resultado de la función "raiz(4)" esto significa que se esta calculando la raiz cuadrada de 4 y el resultado se muestra en el mensaje.
