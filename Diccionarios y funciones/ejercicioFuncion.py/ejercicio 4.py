#se define una funcion llamada 'validarsiexiste' que toma un argumento 'obj'
def validarsiexiste(obj):
    #comprueba si obj es verdadero o falso
    if obj:#en caso de que sea verdadero imprime un mensaje is true
        print(f"{obj} is True")#dato booleano
    else:#en caso de que sea falso imprime un mensaje is false
        print(f"{obj} is False")#dato booleano

validarsiexiste(1)#validando la exitencia de un objeto 
