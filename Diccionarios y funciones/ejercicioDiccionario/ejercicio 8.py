#IMPRIMIR NÚMEROS EN REVERSA
print("Números en reversa")
#la funcion reversed() se utiliza cuando se necesita recorrer una secuencia en orden inverso
for i in reversed(range(1, 10, 2)):#el for recorre y su secuencia empieza desde 9 hasta 1 donde avanza en 2
    print(i)#se imprime el valor de i que en este caso seria los numeros del 1 al 9 en dos 
